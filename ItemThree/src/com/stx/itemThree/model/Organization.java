package com.stx.itemThree.model;

public class Organization {
 private String number;//组织名字
 private String password;//组织密码
 private int org_tel;//组织电话
 private String org_place;//组织地点
 private String org_tech;//团委老师
public String getNumber() {
	return number;
}
public void setNumber(String number) {
	this.number = number;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public int getOrg_tel() {
	return org_tel;
}
public void setOrg_tel(int org_tel) {
	this.org_tel = org_tel;
}
public String getOrg_place() {
	return org_place;
}
public void setOrg_place(String org_place) {
	this.org_place = org_place;
}
public String getOrg_tech() {
	return org_tech;
}
public void setOrg_tech(String org_tech) {
	this.org_tech = org_tech;
}
}
